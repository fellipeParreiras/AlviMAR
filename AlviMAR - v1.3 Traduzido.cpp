void ReceberDadosBluetooth() {
    static bool recebendoDados = false;
    static byte indexDados = 0;
    char delimitadorInicial = '<';
    char delimitadorFinal = '>';
    char caractereRecebido;

    while (bluetooth.available() > 0 && recebeuNovosDados == false) {
        caractereRecebido = bluetooth.read();
        if (recebendoDados == true) {
            if (caractereRecebido != delimitadorFinal) {
                mensagemRecebida[indexDados] = caractereRecebido;
                indexDados++;
                if (indexDados >= numCaracteres) {indexDados = numCaracteres - 1; }
            } else {
                mensagemRecebida[indexDados] = '\0'; // Terminate string
                recebendoDados = false;
                indexDados = 0;
                recebeuNovosDados = true;
            }
        } else if (caractereRecebido == delimitadorInicial) {recebendoDados = true; }
    }
    String mensagem = String(mensagemRecebida);
    valorMensagem = (mensagem.substring(1, mensagem.length())).toFloat();
    instrucaoMensagem = mensagem.substring(0, 1);
}


