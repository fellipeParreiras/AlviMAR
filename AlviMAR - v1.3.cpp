void PerformTourRoutine(float path_values[][2], int wait_times[], String actions[], int startIndex = 0) {
    int distance_index, angle_index, wait_index;
    // Set fist distance index
    while (path_values[distance_index][1] < 0) {
        distance_index++;
        if (distance_index == sizeof(path_values)) {
            break; // There are no movements
        }
    }
    // Set fist angle index
    while (path_values[angle_index][1] != 0) {
        angle_index++;
        if (angle_index == sizeof(path_values)) {
            break; // There are no rotations
        }
    }

    for (int i = startIndex; i < sizeof(actions); i++) { // Perform all actions
        if (actions[i] == "move") {
            MoveRobot(path_values[distance_index][0], path_values[distance_index][1]);
                distance_index++;
                while (path_values[distance_index][1] == 0) { // Ignore angles, set distance_index to next distance
                    distance_index++;
                }
        } else if (actions[i] == "rotate") {
            RotateRobot(path_values[angle_index][1]);
                angle_index++;
                while (path_values[angle_index][1] != 0) { // Ignore angles, set angle_index to next angle
                    angle_index++;
                    if (angle_index == sizeof(path_values)) {
                        break;
                    }
                }
        } else if (actions[i] == "wait") {
            DisplayFace(8, wait_times[wait_index]);
            wait_index++;
        } else if (actions[i] == "speak") {
            
        }
    }
}


void SmartDelay(int delayMilliseconds) {
    int delayClock = 0;

    while (delayClock <= delayMilliseconds) {
        delay(1);
        delayClock++;
        if (bluetooth.available() > 0) {
            return;
        }
    }
}


