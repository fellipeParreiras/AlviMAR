void ReceiveBluetoothData() {
    static bool receivingInProgress = false;
    static byte dataIndex = 0;
    char startMarker = '<';
    char endMarker = '>';
    char receivedChar;

    while (bluetooth.available() > 0 && receivedNewData == false) {
        receivedChar = bluetooth.read();
        if (receivingInProgress == true) {
            if (receivedChar != endMarker) {
                receivedMessage[dataIndex] = receivedChar;
                dataIndex++;
                if (dataIndex >= numChars) {dataIndex = numChars - 1; }
            } else {
                receivedMessage[dataIndex] = '\0'; // Terminate string
                receivingInProgress = false;
                dataIndex = 0;
                receivedNewData = true;
            }
        } else if (receivedChar == startMarker) {receivingInProgress = true; }
    }
    String message = String(receivedMessage);
    messageValue = (message.substring(1, message.length())).toFloat();
    messageInstruction = message.substring(0, 1);
}


void PerformAction() {
    if (messageInstruction == "m") {                     // Move set distance
        MoveRobot(messageValue, robotSpeed);
    } else if (messageInstruction == "c") {              // Move continuously
        MoveRobotContinuous(robotSpeed);
    } else if (messageInstruction == "r") {              // Rotate
        RotateRobot(messageValue);
    } else if (messageInstruction == "h") {              // Rotate neck servo
        neckServo.write(toInt(messageValue));
    } else if (messageInstruction == "f") {              // Change face
        DisplayFace(toInt(messageValue));
    } else if (messageInstruction == "s") {              // Change speed
        robotSpeed = messageValue;
    } else if (messageInstruction == "a") {              // Routine actions request and command from app
        // SendActionsEncoded(actionsUpperLevel, sizeof(actionsUpperLevel) / sizeof(actionsUpperLevel[0]));
        if (toInt(messageValue) != -1) {
            PerformTourRoutine(pathValuesUpper, upperLevelWaitTimes, actionsUpperLevel, messageValue);
        }
    }
}


